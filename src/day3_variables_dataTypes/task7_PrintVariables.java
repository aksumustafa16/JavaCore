package day3_variables_dataTypes;

public class task7_PrintVariables {
    public static void main(String[] args) {
        int i = 10;
        float f = 12.5f;
        String s = "Java Programming";

        System.out.println("i = " + i);
        System.out.println("f = " + f);
        System.out.println("s = " + s);
    }
}
