package day7_controlFlowStatement;

public class task_theNumberIsEqualOrNot {
    public static void main(String[] args) {

        int num1 = 23;
        int num2 = 25;

        if (num1 == num2) {
            System.out.println("These number is equal");
        }
        if (num1 != num2) {
            System.out.println("These number is not equal");
        }
    }
}
