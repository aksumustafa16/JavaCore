package day5_unary_assigment_relational_Operators;

public class assigmentOperators {
    public static void main(String[] args) {

        int i = 100;
        System.out.println("i = " + i);

        i+=1;
        System.out.println("i = " + i);

        i-=2;
        System.out.println("i = " + i);

        i*=3;
        System.out.println("i = " + i);

        i/=4;
        System.out.println("i = " + i);

        i%=5;
        System.out.println("i = " + i);
    }
}
